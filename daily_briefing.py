def _send_via_resend(to_email: str, subject: str, html: str) -> bool:
    """POST the email to the Resend API. Returns True on success."""
    if not _RESEND_KEY:
        logger.warning("RESEND_API_KEY not set — email not sent. Dry-run mode.")
        return False
    try:
        resp = httpx.post(
            "https://api.resend.com/emails",
            headers={"Authorization": f"Bearer {_RESEND_KEY}", "Content-Type": "application/json"},
            json={"from": _FROM_EMAIL, "to": [to_email], "subject": subject, "html": html},
            timeout=15,
        )
        resp.raise_for_status()
        logger.info("Briefing sent to %s (id=%s)", to_email, resp.json().get("id"))
        return True
    except Exception as exc:
        logger.error("Resend delivery failed: %s", exc)
        return False


