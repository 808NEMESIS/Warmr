def generate_and_send(client_id: str, sb: Optional[Client] = None, dry_run: bool = False) -> dict:
    """
    Generate and optionally send the morning briefing for a client.

    Args:
        client_id:  Supabase user UUID.
        sb:         Optional existing Supabase client (injected by the API layer).
        dry_run:    If True, return HTML without sending email.

    Returns dict with keys: sent (bool), html (str), recipient (str).
    """
    if sb is None:
        if not _SUPABASE_URL or not _SUPABASE_KEY:
            raise RuntimeError("SUPABASE_URL and SUPABASE_KEY must be set.")
        sb = create_client(_SUPABASE_URL, _SUPABASE_KEY)

    # Look up client name + email
    client_resp = (
        sb.table("clients")
        .select("company_name, email")
        .eq("id", client_id)
        .limit(1)
        .execute()
    )
    client_row = (client_resp.data or [{}])[0]
    client_name = client_row.get("company_name") or "daar"
    to_email = os.getenv("BRIEFING_TO_EMAIL") or client_row.get("email", "")

    # Gather data
    inbox_data = _gather_inbox_data(sb, client_id)
    campaigns = _gather_campaign_data(sb, client_id)
    experiments = _gather_experiment_data(sb, client_id)
    pending_decisions = _gather_pending_decisions(sb, client_id)

    # AI focus block
    focus_block = _generate_focus_block(inbox_data, campaigns, experiments, pending_decisions)

    # Build HTML
    html = _build_html(client_name, inbox_data, campaigns, experiments, pending_decisions, focus_block)

    if dry_run:
        return {"sent": False, "html": html, "recipient": to_email}

    sent = _send_via_resend(to_email, _L["subject"], html)
    return {"sent": sent, "html": html, "recipient": to_email}


