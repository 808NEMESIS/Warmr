def _generate_focus_block(
    inbox_data: dict,
    campaigns: list[dict],
    experiments: list[dict],
    pending_decisions: list[dict],
    supabase_client=None,
    client_id: str | None = None,
) -> str:
    """Ask Claude Sonnet for a 2–3 sentence focus recommendation in the configured language."""
    if not _ANTHROPIC_KEY:
        return ""

    lang_instruction = {
        "nl": "Schrijf in het Nederlands.",
        "en": "Write in English.",
        "fr": "Écris en français.",
    }.get(_LANGUAGE, "Schrijf in het Nederlands.")

    alerts = []
    if inbox_data["paused"] > 0:
        alerts.append(f"{inbox_data['paused']} inbox(en) gepauzeerd")
    if inbox_data["spam_complaints"] > 0:
        alerts.append(f"{inbox_data['spam_complaints']} spam klacht(en)")
    for c in campaigns:
        if c.get("alert"):
            alerts.append(f"Bounce rate te hoog op campagne '{c['name']}': {c['bounce_rate']}%")
    for exp in experiments:
        if exp["progress_pct"] >= 100:
            alerts.append(f"Experiment '{exp['name']}' heeft genoeg data — klaar voor conclusie")

    context_lines = [
        f"Inbox gezondheid: {inbox_data['total']} inboxes, gemiddelde reputatie {inbox_data['avg_reputation']}/100, {inbox_data['ready']} klaar voor campagnes.",
        "Campagnes (top): " + (', '.join(f"{c['name']} ({c['reply_rate']}% reply)" for c in campaigns[:3]) or 'geen actief') + ".",
        f"Actieve experimenten: {len(experiments)}.",
        f"Openstaande beslissingen zonder effect-meting: {len(pending_decisions)}.",
    ]
    if alerts:
        context_lines.append(f"Aandachtspunten: {'; '.join(alerts)}.")

    prompt = (
        "Je bent de AI-assistent van een email deliverability platform. "
        "Hieronder staat de huidige staat van het systeem.\n\n"
        + "\n".join(context_lines)
        + "\n\nGeef in 2 à 3 zinnen het meest belangrijke focuspunt voor vandaag. "
        "Wees concreet en actiegericht. Geen opsommingen, geen markdown. "
        + lang_instruction
    )

    try:
        client = Anthropic(api_key=_ANTHROPIC_KEY)
        messages = [{"role": "user", "content": prompt}]

        if supabase_client:
            from utils.cost_tracker import tracked_claude_call
            response = tracked_claude_call(
                client, supabase_client,
                model="claude-sonnet-4-6",
                messages=messages,
                max_tokens=200,
                context="daily_briefing",
                client_id=client_id,
            )
        else:
            response = client.messages.create(
                model="claude-sonnet-4-6",
                max_tokens=200,
                messages=messages,
            )
        return response.content[0].text.strip()
    except Exception as exc:
        logger.error("Claude focus generation failed: %s", exc)
        return ""


