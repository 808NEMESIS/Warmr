def _gather_inbox_data(sb: Client, client_id: str) -> dict:
    """Return inbox health summary for the client."""
    resp = (
        sb.table("inboxes")
        .select("email, reputation_score, status, warmup_active, spam_complaints, daily_sent")
        .eq("client_id", client_id)
        .execute()
    )
    inboxes = resp.data or []
    ready = [i for i in inboxes if i["status"] == "ready"]
    warming = [i for i in inboxes if i["status"] == "warmup"]
    paused = [i for i in inboxes if not i.get("warmup_active") or i["status"] == "paused"]
    avg_rep = round(sum(i.get("reputation_score", 0) for i in inboxes) / len(inboxes), 1) if inboxes else 0
    complaints_today = sum(i.get("spam_complaints", 0) for i in inboxes)

    return {
        "total": len(inboxes),
        "ready": len(ready),
        "warming": len(warming),
        "paused": len(paused),
        "avg_reputation": avg_rep,
        "spam_complaints": complaints_today,
        "rows": inboxes,
    }


def _gather_campaign_data(sb: Client, client_id: str) -> list[dict]:
    """Return top 5 campaigns by reply rate with basic stats."""
    # Get active campaigns
    camp_resp = (
        sb.table("campaigns")
        .select("id, name, status, total_leads")
        .eq("client_id", client_id)
        .in_("status", ["active", "sending"])
        .order("created_at", desc=True)
        .limit(10)
        .execute()
    )
    campaigns = camp_resp.data or []
    if not campaigns:
        return []

    since = (datetime.now(timezone.utc) - timedelta(days=7)).isoformat()
    results = []
    for c in campaigns:
        sched_resp = (
            sb.table("sending_schedule")
            .select("status")
            .eq("campaign_id", c["id"])
            .gte("sent_at", since)
            .execute()
        )
        rows = sched_resp.data or []
        sent = sum(1 for r in rows if r["status"] in ("sent", "replied", "bounced"))
        replied = sum(1 for r in rows if r["status"] == "replied")
        bounced = sum(1 for r in rows if r["status"] == "bounced")
        reply_rate = round(replied / sent * 100, 1) if sent else 0
        bounce_rate = round(bounced / sent * 100, 1) if sent else 0
        results.append({
            "name": c["name"],
            "status": c["status"],
            "sent_7d": sent,
            "reply_rate": reply_rate,
            "bounce_rate": bounce_rate,
            "alert": bounce_rate > 3,
        })

    results.sort(key=lambda x: x["reply_rate"], reverse=True)
    return results[:5]


def _gather_experiment_data(sb: Client, client_id: str) -> list[dict]:
    """Return active experiments with progress toward min_sample_size."""
    resp = (
        sb.table("experiments")
        .select("id, name, hypothesis, metric, min_sample_size, status, started_at, control_campaign_id, variant_campaign_id")
        .eq("client_id", client_id)
        .eq("status", "active")
        .execute()
    )
    experiments = resp.data or []
    results = []
    for exp in experiments:
        # Count sends for both campaigns combined
        total_sends = 0
        for camp_id in [exp.get("control_campaign_id"), exp.get("variant_campaign_id")]:
            if camp_id:
                r = (
                    sb.table("sending_schedule")
                    .select("id", count="exact")
                    .eq("campaign_id", camp_id)
                    .in_("status", ["sent", "replied", "bounced"])
                    .execute()
                )
                total_sends += r.count or 0
        pct = round(total_sends / exp["min_sample_size"] * 100) if exp.get("min_sample_size") else 0
        results.append({
            "name": exp["name"],
            "hypothesis": exp.get("hypothesis", ""),
            "metric": exp.get("metric", "reply_rate"),
            "progress_pct": min(pct, 100),
            "total_sends": total_sends,
            "min_sample": exp.get("min_sample_size", 100),
        })
    return results


def _gather_pending_decisions(sb: Client, client_id: str) -> list[dict]:
    """Return recent decision_log entries whose effect has not been calculated yet."""
    cutoff = (datetime.now(timezone.utc) - timedelta(days=14)).isoformat()
    resp = (
        sb.table("decision_log")
        .select("id, decision_type, entity_type, entity_name, reason, created_at")
        .eq("client_id", client_id)
        .is_("effect", "null")
        .gte("created_at", cutoff)
        .order("created_at", desc=True)
        .limit(5)
        .execute()
    )
    return resp.data or []


